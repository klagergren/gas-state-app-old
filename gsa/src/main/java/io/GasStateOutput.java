package io;

public class GasStateOutput {

	private double temperature;
	private double pressure;
	private double internalEnergy;
	private double enthalpy;
	private double soundSpeed;
	private double density;
	private double entropy;
	
	public double getTemperature() {
		return temperature;
	}
	public void setTemperature(double temperature) {
		this.temperature = temperature;
	}
	public double getPressure() {
		return pressure;
	}
	public void setPressure(double pressure) {
		this.pressure = pressure;
	}
	public double getInternalEnergy() {
		return internalEnergy;
	}
	public void setInternalEnergy(double internalEnergy) {
		this.internalEnergy = internalEnergy;
	}
	public double getEnthalpy() {
		return enthalpy;
	}
	public void setEnthalpy(double enthalpy) {
		this.enthalpy = enthalpy;
	}
	public double getSoundSpeed() {
		return soundSpeed;
	}
	public void setSoundSpeed(double soundSpeed) {
		this.soundSpeed = soundSpeed;
	}
	public double getDensity() {
		return density;
	}
	public void setDensity(double density) {
		this.density = density;
	}
	public double getEntropy() {
		return entropy;
	}
	public void setEntropy(double entropy) {
		this.entropy = entropy;
	}

}
